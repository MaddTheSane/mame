/**************
 * Write in the video ram for the blitter
 */
void williams_videoram_blitter_w(int offset,int data,int flag)
{
int x,y;
int p1,p2,pb1,pb2;


/*  Calcul the real x-y
 */

  x = offset % 256;
  y = offset / 256;
#ifdef HEIGHT_240
  x -= START_OFFSET;
  if(x<0)
    return;
  if(x>=240)
    return;
#endif

/*   Source pixels
 */

 p1 = data >> 4;
 p2 = data & 0x0F;

/*
 1000 0000 Do not process half the byte 4-7
 0100 0000 Do not process half the byte 0-3
 0010 0000 Shift the shape one pixel right (to display a shape on an odd pixel)
 0001 0000 Remap, if shape != 0 then = mask
 0000 1000 Source  1 = take source 0 = take Mask only
 0000 0100
 0000 0010 Transparent
 0000 0001
*/

/*  Background pixels
 */

  if((flag & 0x20) == 0){
    pb1 = GetPix(x,(y*2));
    pb2 = GetPix(x,(y*2)+1);
  }else{
    pb1 = GetPix(x,(y*2)+1);
    pb2 = GetPix(x,(y*2)+2);
  }


/*  Not optimized yet, there must be a way to do that faster
 */
	if((flag & 0x08) == 0){
		if((flag & 0x10) == 0){
			if((flag & 0x80) == 0)
				pb1 = p1;
			if((flag & 0x40) == 0)
				pb2 = p2;
		}else{
			if((flag & 0x80) == 0)
				pb1 = RAM[0xCA01]>>4;
			if((flag & 0x40) == 0)
				pb2 = RAM[0xCA01]&0x0F;
		}
	}else{
		if((flag & 0x10) == 0){
			if(p1 != 0)
				if((flag & 0x80) == 0)
					pb1 = p1;
			if(p2 != 0)
				if((flag & 0x40) == 0)
					pb2 = p2;
		}else{
			if(p1 != 0)
				if((flag & 0x80) == 0)
					pb1 = RAM[0xCA01]>>4;
			if(p2 != 0)
				if((flag & 0x40) == 0)
					pb2 = RAM[0xCA01]&0x0f;
		}
	}


/*   Clip if outside the video then it is in RAM[]
 *   Bubble blit some code at $9800 +
 */

  if(offset>>8 >= 0x98){

/*   But do not write on ROM, Joust blit at $FXXX when a shape pass the
 *   left of the screen
 */

    if(offset>>8 >= 0xE0)
      return;
    RAM[offset] = data;
    return;
  }

/*  Put the byte in the videoram
 *  Not really good if flag & 0x20 != 0
 *  But the games seem to never read the video ram.
 */
	williams_videoram[offset] = (pb1<<4) + pb2;

/*  Put the pixels in our bitmap
 */

  if((flag & 0x20) == 0){
    PutPix(x,(y*2),pb1);
    PutPix(x,(y*2)+1,pb2);
  }else{
    PutPix(x,(y*2)+1,pb1);
    PutPix(x,(y*2)+2,pb2);
  }

}



